def get_all_competitor_urls():
    rows = []
    for brand, info in COMPETITOR_LIBRARY.items():
        for category, urls in info["categories"].items():
            for url in urls:
                rows.append({
                    "brand": brand,
                    "tier": info["tier"],
                    "category": category,
                    "url": url,
                    "domain": info["domain"],
                    "color": info.get("color", "#666"),
                })
    return rows


def get_brands():
    return list(COMPETITOR_LIBRARY.keys())


def get_brands_by_tier():
    tiers = {}
    for brand, info in COMPETITOR_LIBRARY.items():
        t = info["tier"]
        tiers.setdefault(t, []).append(brand)
    return tiers


def get_categories():
    cats = set()
    for info in COMPETITOR_LIBRARY.values():
        cats.update(info["categories"].keys())
    return sorted(cats)


def get_urls_for_brand(brand):
    info = COMPETITOR_LIBRARY.get(brand, {})
    urls = []
    for cat_urls in info.get("categories", {}).values():
        urls.extend(cat_urls)
    return urls


def get_urls_for_category(category):
    rows = []
    for brand, info in COMPETITOR_LIBRARY.items():
        if category in info["categories"]:
            for url in info["categories"][category]:
                rows.append({"brand": brand, "tier": info["tier"],
                             "category": category, "url": url})
    return rows
